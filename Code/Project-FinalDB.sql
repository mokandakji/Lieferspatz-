BEGIN TRANSACTION;
CREATE TABLE IF NOT EXISTS "Account" (
	"Username"	VARCHAR(255),
	"Password"	VARCHAR(255),
	"UserType"	VARCHAR(20) NOT NULL,
	PRIMARY KEY("Username")
);
CREATE TABLE IF NOT EXISTS "Customer" (
	"CustomerID"	INTEGER,
	"Username"	VARCHAR(255) UNIQUE,
	"FirstName"	VARCHAR(255),
	"LastName"	VARCHAR(255),
	"Address"	VARCHAR(255),
	"PLZ"	VARCHAR(10),
	"City"	VARCHAR(255),
	PRIMARY KEY("CustomerID" AUTOINCREMENT),
	FOREIGN KEY("Username") REFERENCES "Account"("Username")
);
CREATE TABLE IF NOT EXISTS "Serve" (
	"RestaurantID"	INT,
	"MenuItemID"	INT,
	PRIMARY KEY("RestaurantID","MenuItemID"),
	FOREIGN KEY("RestaurantID") REFERENCES "Restaurant"("RestaurantID"),
	FOREIGN KEY("MenuItemID") REFERENCES "MenuItem"("MenuItemID")
);
CREATE TABLE IF NOT EXISTS "AddItem" (
	"CartID"	INT,
	"MenuItemID"	INT,
	PRIMARY KEY("CartID","MenuItemID"),
	FOREIGN KEY("CartID") REFERENCES "Cart"("CartID"),
	FOREIGN KEY("MenuItemID") REFERENCES "MenuItem"("MenuItemID")
);
CREATE TABLE IF NOT EXISTS "Has" (
	"CustomerID"	INT,
	"OrderID"	INT,
	PRIMARY KEY("CustomerID","OrderID"),
	FOREIGN KEY("OrderID") REFERENCES "OrderTable"("OrderID"),
	FOREIGN KEY("CustomerID") REFERENCES "Customer"("CustomerID")
);
CREATE TABLE IF NOT EXISTS "Checkout" (
	"CartID"	INT,
	"OrderID"	INT,
	PRIMARY KEY("CartID","OrderID"),
	FOREIGN KEY("CartID") REFERENCES "Cart"("CartID"),
	FOREIGN KEY("OrderID") REFERENCES "OrderTable"("OrderID")
);
CREATE TABLE IF NOT EXISTS "OrderTable" (
	"OrderID"	INTEGER,
	"Status"	VARCHAR(255),
	"SubmissionTime"	DATETIME,
	"ReviewedBy"	INT,
	"ReceivedBy"	INT,
	PRIMARY KEY("OrderID" AUTOINCREMENT),
	FOREIGN KEY("ReviewedBy") REFERENCES "Customer"("CustomerID"),
	FOREIGN KEY("ReceivedBy") REFERENCES "Restaurant"("RestaurantID")
);
CREATE TABLE IF NOT EXISTS "Codes" (
	"RestaurantID"	INT,
	"Code"	INT,
	FOREIGN KEY("RestaurantID") REFERENCES "Restaurant"("RestaurantID")
);
CREATE TABLE IF NOT EXISTS "MenuItem" (
	"MenuItemID"	INTEGER,
	"RestaurantID"	INT,
	"Name"	VARCHAR(255),
	"Category"	VARCHAR(255),
	"Description"	VARCHAR(255),
	"Price"	DECIMAL(10, 2),
	"Picture"	VARCHAR(255),
	PRIMARY KEY("MenuItemID" AUTOINCREMENT),
	FOREIGN KEY("RestaurantID") REFERENCES "Restaurant"("RestaurantID")
);
CREATE TABLE IF NOT EXISTS "Cart" (
	"CartID"	INTEGER,
	"CustomerID"	INT,
	"ItemID"	INT,
	"Quantity"	INT,
	"TotalPrice"	DECIMAL(10, 2),
	PRIMARY KEY("CartID" AUTOINCREMENT),
	FOREIGN KEY("CustomerID") REFERENCES "Customer"("CustomerID"),
	FOREIGN KEY("ItemID") REFERENCES "MenuItem"("MenuItemID")
);
CREATE TABLE IF NOT EXISTS "OrderDetails" (
	"DetailID"	INTEGER,
	"OrderID"	INT,
	"ItemID"	INT,
	"Quantity"	INT,
	"TotalPrice"	DECIMAL(10, 2),
	PRIMARY KEY("DetailID" AUTOINCREMENT),
	FOREIGN KEY("OrderID") REFERENCES "OrderTable"("OrderID"),
	FOREIGN KEY("ItemID") REFERENCES "MenuItem"("MenuItemID")
);
CREATE TABLE IF NOT EXISTS "Restaurant" (
	"RestaurantID"	INTEGER,
	"Username"	VARCHAR(255) UNIQUE,
	"FirstName"	VARCHAR(255),
	"LastName"	VARCHAR(255),
	"Address"	VARCHAR(255),
	"PLZ"	VARCHAR(10),
	"City"	VARCHAR(255),
	"Description"	VARCHAR(255),
	"Picture"	VARCHAR(255),
	"OpeningTime"	TIME,
	"ClosingTime"	TIME,
	PRIMARY KEY("RestaurantID" AUTOINCREMENT),
	FOREIGN KEY("Username") REFERENCES "Account"("Username")
);
